
public class Secret {
    private String target;
    private String key;

    public String getTarget() {
        return this.target;
    }

    public String getKey() {
        return this.key;
    }

    public void setTarget(String target) {
        this.target = target;
    }

    public void setKey(String key) {
        this.key = key;
    }
}